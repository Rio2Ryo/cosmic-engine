// Cosmic Engine - Client App
const API_BASE = 'http://localhost:3030';

async function loadStatus() {
  try {
    const res = await fetch(`${API_BASE}/api/status`);
    const data = await res.json();
    document.getElementById('agent-count').textContent = data.running_agents || 0;
    document.getElementById('task-count').textContent = data.total_tasks || 0;
  } catch (e) {
    console.log('Status API not available');
  }
}

// Event Source for real-time updates
let eventSource = null;

function connectSSE() {
  if (eventSource) eventSource.close();
  eventSource = new EventSource(`${API_BASE}/api/events`);
  
  eventSource.onmessage = (event) => {
    try {
      const data = JSON.parse(event.data);
      const log = document.getElementById('activity-log');
      const entry = document.createElement('div');
      entry.className = `log-entry ${data.level || 'info'}`;
      entry.textContent = `[${data.timestamp || ''}] ${data.message || ''}`;
      log.insertBefore(entry, log.firstChild);
      
      // Keep max 50 entries
      while (log.children.length > 50) {
        log.removeChild(log.lastChild);
      }
    } catch (e) {}
  };
  
  eventSource.onerror = () => {
    setTimeout(connectSSE, 3000);
  };
}

loadStatus();
connectSSE();
setInterval(loadStatus, 5000);
